#include <iostream>
#include <windows.h>
#include <time.h>
using namespace std;
void main()
{
    SetConsoleCP(1251);//Включаем русский язык
    SetConsoleOutputCP(1251);
    srand(unsigned(time(NULL)));//Сброс генератора
    int a, b, i, min, max;//Инициализация переменных
    cout << "Игра \"Угадай число\".\n\n";
    cout << "Введите минимальное число: ";
    cin >> min;
    system("cls");
    cout << "Игра \"Угадай число\".\n\n";
    cout << "Введите максимальное число: ";
    cin >> max;
    system("cls");
    cout << "Игра \"Угадай число\".\n\n";
    cout << "Количество жизней: ";
    cin >> i;
    system("cls");

    a = rand() % ((min - max) - min )+ 1;//Генерируем число от 1 до 10
    cout << "Игра \"Угадай число\".\n\n";
    cout << "Компьютер \"задумал\" число от " << min << " до " << max << ".\n";
    cout << "У вас имеется " << i << " попыток.\n\n";
    cout << "Введите число и нажмите <Enter>\n\n";
    for (i < 1;i--;)//Цикл проверки
    {
        cout << "> ";
        cin >> b;//Ввод числа
        if (b == a)//Сравнение чисел
        {
            system("cls");
            cout << "Игра \"Угадай число\".\n\n";
            cout << "[+] Вы выиграли! Поздравляю!\n";
            cout << "[+] У вас осталось " << i << " попыток!\n";
            break;
        }
        if (b > a)
        {
            cout << "[-] Нет, введённое Вами число, больше \"задуманного\" компьютером\n";
        }
        if (b < a)
        {
            cout << "[-] Нет, введённое Вами число, меньше \"задуманного\" компьютером\n";
        }
    }
    if (b != a && i < 1)//Проверка
    {
        system("cls");
        cout << "Игра \"Угадай число\".\n\n";
        cout << "[-] К сожалению Вы так и не смогли угадать число...\n";
    }
}